package com.tj.ex4insert;
import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;
import java.util.Scanner;
public class ExOracleInsert2 {
	public static void main(String[] args) {
		String driver = "oracle.jdbc.driver.OracleDriver";
		String url = "jdbc:oracle:thin:@127.0.0.1:1521:xe";
		Connection conn = null;
		Statement  stmt = null;
		ResultSet  rs   = null;
		Scanner sc = new Scanner(System.in);
		System.out.print("입력할 부서번호는 ?");
		int deptno = sc.nextInt();
		// 입력한 부서번호가 있는지 확인 SQL
		String sqlSelect = "SELECT * FROM DEPT WHERE DEPTNO="+deptno;
		try {
			Class.forName(driver);
			conn = DriverManager.getConnection(url, "scott", "tiger");
			stmt = conn.createStatement();
			rs   = stmt.executeQuery(sqlSelect);
			if(rs.next()) {
				System.out.println("같은 부서번호가 있어서 입력 불가합니다");
			}else {
				System.out.print("그 부서의 부서명은? ");
				String dname = sc.next();
				System.out.print("그 부서의 위치는 ?");
				String loc = sc.next();
				//String sql = "INSERT INTO DEPT VALUES ("+deptno+", '"+dname+"','"+loc+"')";
				String sql = String.format("INSERT INTO DEPT VALUES (%d, '%s','%s')", 
																  deptno, dname, loc);
				int result = stmt.executeUpdate(sql);
				if(result>0) {
					System.out.println(deptno+"번 부서 입력 성공");
				}				
			}
		} catch (ClassNotFoundException e) {
			System.out.println(e.getMessage());
		} catch (SQLException e) {
			System.out.println(deptno+"번 부서 입력 실패"+e.getMessage());
		} finally {
			try {
				if(rs!=null) rs.close();
				if(stmt!=null) stmt.close();
				if(conn!=null) conn.close();
			} catch (Exception e2) {}
		}
	}
}






















